-module(c_time).
-author("chenkecai").

%% API
-export([now_millisecond/0, now_second/0]).

%%%===================================================================
%%% 获取当前系统毫秒
%%%===================================================================
now_millisecond() ->
    {M, S, MS} = os:timestamp(),
    M * 1000000000 + S * 1000 + MS div 1000.

%%%===================================================================
%%% 获取当前秒
%%%===================================================================
now_second() ->
    {M, S, _} = os:timestamp(),
    M * 1000000 + S.
