-module(term_zip).
-author("ckc").

%% API
-export([zip/1, unzip/1]).

%%%===================================================================
%% @doc
%%
%% @end
%%%===================================================================
zip(Data) ->
    Str = str_lib:term_2_str(Data),
    zlib:zip(Str).

%%%===================================================================
%% @doc
%%
%% @end
%%%===================================================================
unzip(Bin) when is_binary(Bin) ->
    Data = zlib:unzip(Bin),
    str_lib:str_2_term(binary_to_list(Data)).